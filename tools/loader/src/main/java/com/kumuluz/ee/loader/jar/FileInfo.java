package com.kumuluz.ee.loader.jar;

import java.io.File;

/**
 * @author Benjamin Kastelic, Sunesis ltd.
 * @since 1.0.0
 */
public class FileInfo {

    private File file;
    private String simpleName;

    public FileInfo(File file, String simpleName) {
        this.simpleName = simpleName;
        if (this.simpleName.endsWith("/") && this.simpleName.length() > 1) {
            this.simpleName = this.simpleName.substring(0 ,this.simpleName.length() - 1);
        }

        this.file = file;
    }

    public File getFile() {
        return file;
    }

    public void setFile(File file) {
        this.file = file;
    }

    public String getSimpleName() {
        return simpleName;
    }

    public void setSimpleName(String simpleName) {
        this.simpleName = simpleName;
    }
}
